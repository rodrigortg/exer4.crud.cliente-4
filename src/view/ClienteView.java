    private void jButton1AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1AlterarActionPerformed
       
         if(jTable1Clientes.getSelectedRow() != -1){
           
             clienteController.update((int)jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 0),
                  jTextFieldNome.getText(),
                  jTextField1Email.getText(),
                  jFormattedTextFieldCPF.getText(),
                  jFormattedTextFieldTelefone.getText(),
                  jFormattedTextField1DataAniversario.getText());
           
        this.getListaClientes();
          
        limpaCampos();
       } 
        
    }//GEN-LAST:event_jButton1AlterarActionPerformed

    private void jButton2ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ExcluirActionPerformed
     
          if(jTable1Clientes.getSelectedRow() != -1){
          clienteController.delete((int)jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 0));
                           
          this.getListaClientes();
          
          limpaCampos();
       }else{
            JOptionPane.showMessageDialog(null,"Seleciona um cliente para excluir!");
        } 
        
        
    }//GEN-LAST:event_jButton2ExcluirActionPerformed

