    private void jTable1ClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1ClientesMouseClicked
  
        if(jTable1Clientes.getSelectedRow() != -1){
           jTextFieldNome.setText(jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 1).toString());
           jTextField1Email.setText(jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 2).toString());
           jFormattedTextFieldCPF.setText(jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 3).toString());
           jFormattedTextFieldTelefone.setText(jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 4).toString());
           jFormattedTextField1DataAniversario.setText(jTable1Clientes.getValueAt(jTable1Clientes.getSelectedRow(), 5).toString());
          
        }
        
    }//GEN-LAST:event_jTable1ClientesMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
        DefaultTableModel modeloTableClientes = (DefaultTableModel) jTable1Clientes.getModel();
        modeloTableClientes.setNumRows(0);
        
        for (Cliente c: clienteController.getListaClientesNome(jTextField1Busca.getText())) {
            
            modeloTableClientes.addRow(new Object[]{
               c.getIdcliente(),
               c.getNome().trim(),
               c.getEmail(),
               c.getCpf(),
               c.getTelefone(),
               c.getDataaniversario()});   
        }
    }//GEN-LAST:event_jButton3ActionPerformed

